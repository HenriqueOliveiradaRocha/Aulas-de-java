package calculos;

public class FuncoesMatematicas {
	public double soma(double a, double b) {
		double total = a + b;
		return total;
	}
	
	public double subtrair(double a, double b) {
		double total = a - b;
		return total;
	}
	
	public double dividir(double a, double b) {
		double total = a / b;
		return total;
	}
	
	public double multiplicacao(double a, double b) {
		double total = a * b;
		return total;
	}
	
	public double porcCal(double a) {
		double contaPorc = a /100; 
		return contaPorc;
	}
}
